#include <string.h>
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*str;

	i = 0;
	if (!(str = (char *)malloc(size + 1 * sizeof(char))))
		return (NULL);
	while (i <= size)
		str[i++] = '\0';
	return (str);
}
